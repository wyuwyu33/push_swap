#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t count)
{
	size_t	dest_len;
	size_t	src_len;

	dest_len = ft_strlen(dest);
	src_len = ft_strlen(src);
	if (count < dest_len + 1)
		return (count + src_len);
	ft_strlcpy(dest + dest_len, src, count - dest_len);
	return (dest_len + src_len);
}
