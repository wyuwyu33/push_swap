#include "../inc/push_swap.h"

int ft_arr_sortcheck(t_arr *arr)
{
	int i;

	i = -1;
	while (++i < arr->size - 1)
	{
		if (arr->val[i] > arr->val[i + 1])
			return (0);
	}
	return (1);
}